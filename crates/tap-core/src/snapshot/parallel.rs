/// Run a parallel, chunked snapshot of Postgres tables.
///
/// # Architecture
///
/// A persistent **run_id** (UUID v4) is generated at the start of each
/// snapshot run and stored in [`StateStore`] instance info.  After a crash,
/// the next invocation detects the stale run_id and resumes incomplete
/// chunks rather than starting from scratch.
///
/// The Postgres-level exported snapshot ID (from `pg_export_snapshot()`)
/// is kept separate — it changes every connection but does not affect chunk
/// tracking, which uses the persistent `run_id`.
///
/// # Arguments
///
/// * `keeper` — Connection that holds the exported snapshot for the full
///   duration.
/// * `source_config` — Postgres source config (used to spawn worker
///   connections and resolve publication tables).
/// * `state` — Shared state store for checkpointing chunk progress.
/// * `config` — Snapshot configuration (batch_size, num_workers, tables).
/// * `db_name` — Database name (for source metadata in events).
/// * `event_tx` — Channel for emitting `ChangeEvent`s.
///
/// # Errors
///
/// Propagates Postgres, SQLite, and snapshot-specific errors from any
/// worker or the keeper.  If a worker fails, the remaining workers are
/// allowed to finish their current chunk, then the error is returned.
///
/// # Example (conceptual)
///
/// ```rust,ignore
/// use tap_core::snapshot::parallel::run_parallel_snapshot;
///
/// let result = run_parallel_snapshot(
///     keeper, &source_config, state, &snap_config, "mydb", event_tx
/// ).await?;
/// ```
pub async fn run_parallel_snapshot(
    keeper: tokio_postgres::Client,
    source_config: &SourceConfig,
    state: Arc<Mutex<StateStore>>,
    config: &SnapshotConfig,
    db_name: String,
    event_tx: UnboundedSender<ChangeEvent>,
) -> Result<SnapshotResult, TapError> {
    // ── 0. Resolve persistent run_id (resume across restarts) ────
    let run_id = resolve_run_id(&state).await?;
    info!("snapshot run_id={run_id}");

    // ── 1. Begin keeper transaction & export snapshot ─────────────
    keeper
        .batch_execute("BEGIN READ ONLY ISOLATION LEVEL REPEATABLE READ")
        .await?;

    let export_result = export_snapshot_inner(&keeper).await;

    match export_result {
        Ok((pg_snapshot_id, lsn)) => {
            info!("pg_snapshot exported: id={pg_snapshot_id}, lsn={lsn}");

            let result = run_snapshot_inner(
                &keeper,
                source_config,
                &state,
                &run_id,
                &pg_snapshot_id,
                lsn,
                config.num_workers,
                config.batch_size as u32,
                &db_name,
                &event_tx,
            )
            .await;

            // Always close keeper
            keeper
                .batch_execute(match &result {
                    Ok(_) => "COMMIT",
                    Err(_) => "ROLLBACK",
                })
                .await
                .ok();

            result
        }
        Err(e) => {
            keeper.batch_execute("ROLLBACK").await.ok();
            Err(e)
        }
    }
}

// ---------------------------------------------------------------------------
// Resume detection
// ---------------------------------------------------------------------------

/// Resolve a persistent run-id for this snapshot run.
///
/// Checks the state store for a previously stored `current_run_id`.  If one
/// exists and has incomplete (non-completed) chunks, returns that id so the
/// caller resumes from where the previous run left off.  Otherwise generates
/// a fresh UUID and persists it.
async fn resolve_run_id(state: &Arc<Mutex<StateStore>>) -> Result<String, TapError> {
    let store = state.lock().await;
    let stored = store.get_instance_info("current_run_id")?;
    match stored {
        Some(id) if !id.is_empty() => {
            let incomplete = store.count_incomplete_chunks(&id)?;
            if incomplete > 0 {
                return Ok(id); // Resume mode
            }
            // Stale entry — all chunks completed or no chunks exist.
            // Overwrite with a fresh run_id.
            let new_id = uuid::Uuid::new_v4().to_string();
            store.set_instance_info("current_run_id", &new_id)?;
            Ok(new_id)
        }
        _ => {
            let new_id = uuid::Uuid::new_v4().to_string();
            store.set_instance_info("current_run_id", &new_id)?;
            Ok(new_id)
        }
    }
}

// ---------------------------------------------------------------------------
// Core orchestration
// ---------------------------------------------------------------------------

/// The inner snapshot loop (keeper transaction already active).
///
/// * `run_id` — Persistent chunk-grouping key (survives restarts for resume).
/// * `pg_snapshot_id` — Postgres export-snapshot ID for `SET TRANSACTION
///   SNAPSHOT` in worker connections.
#[allow(clippy::too_many_arguments)]
async fn run_snapshot_inner(
    keeper: &tokio_postgres::Client,
    source_config: &SourceConfig,
    state: &Arc<Mutex<StateStore>>,
    run_id: &str,
    pg_snapshot_id: &str,
    lsn: Lsn,
    num_workers: u32,
    batch_size: u32,
    db_name: &str,
    event_tx: &UnboundedSender<ChangeEvent>,
) -> Result<SnapshotResult, TapError> {
    // ── 2. Resolve tables (using keeper, since it has the tx) ────
    let tables = resolve_tables_inner(keeper, source_config).await?;
    if tables.is_empty() {
        info!("no tables to snapshot");
        return Ok(SnapshotResult {
            snapshot_id: run_id.to_string(),
            position: lsn,
            total_rows: 0,
            tables_snapshotted: Vec::new(),
        });
    }

    // ── 3. Generate chunks for each table ─────────────────────────
    let mut all_chunks: Vec<SnapshotChunk> = Vec::new();
    let mut tables_snapshotted: Vec<String> = Vec::with_capacity(tables.len());

    for table in &tables {
        let pk_cols = detect_pk_columns(keeper, table).await?;
        let pk_str = pk_cols.first().cloned().unwrap_or_default();

        // Load existing chunks for resume (keyed by persistent run_id)
        let existing = {
            let store = state.lock().await;
            store.get_table_chunks(run_id, &table.qualified)?
        };

        let chunks = if pk_str.is_empty() {
            // No PK — single chunk only
            let num_chunks = 1u32;
            generate_chunks(table, run_id, "", None, num_chunks, &existing)
        } else {
            let pk_range = query_pk_range(keeper, table, &pk_str).await?;
            let num_per_table = (num_workers * 2).max(1);
            generate_chunks(
                table,
                run_id,
                &pk_str,
                pk_range.as_ref(),
                num_per_table,
                &existing,
            )
        };

        // Register NEW chunks (skipped completed ones are not returned)
        {
            let store = state.lock().await;
            for chunk in &chunks {
                store.write_chunk(
                    &chunk.table_name,
                    &chunk.snapshot_id,
                    chunk.chunk_index,
                    chunk.chunk_start.as_deref(),
                    chunk.chunk_end.as_deref(),
                )?;
            }
        }

        // ── 3a. Detect chunk-size skew (pre-flight) ─────────────
        if chunks.len() > 1 && !pk_str.is_empty() {
            detect_chunk_skew(keeper, table, &pk_str, &chunks).await?;
        }

        all_chunks.extend(chunks);
        tables_snapshotted.push(format!("\"{}\".\"{}\"", table.schema, table.name));
    }

    if all_chunks.is_empty() {
        info!("all chunks already completed (resume)");
        // Reconstruct result from state store
        let total_rows = {
            let store = state.lock().await;
            store.snapshot_chunk_total_rows(run_id)?
        };
        return Ok(SnapshotResult {
            snapshot_id: run_id.to_string(),
            position: lsn,
            total_rows,
            tables_snapshotted,
        });
    }

    info!(
        total_chunks = all_chunks.len(),
        workers = num_workers,
        "starting parallel snapshot"
    );

    // ── 4. Distribute chunks via mpsc channel ────────────────────
    let num_chunks = all_chunks.len() as u32;
    let (work_tx, work_rx) = mpsc::channel::<SnapshotChunk>(num_chunks as usize);
    let work_rx = Arc::new(Mutex::new(work_rx));

    for chunk in all_chunks {
        work_tx
            .send(chunk)
            .await
            .map_err(|_| TapError::Snapshot("failed to enqueue chunk into work queue".into()))?;
    }
    // Drop the sender so receivers see None when all chunks are consumed
    drop(work_tx);

    // ── 5. Spawn worker tasks ────────────────────────────────────
    let mut handles = Vec::with_capacity(num_workers as usize);
    let error_flag = Arc::new(std::sync::atomic::AtomicBool::new(false));

    for worker_id in 0..num_workers {
        let work_rx = Arc::clone(&work_rx);
        let state = Arc::clone(state);
        let event_tx = event_tx.clone();
        let r_id = run_id.to_string();
        let pg_id = pg_snapshot_id.to_string();
        let db_name = db_name.to_string();
        let error_flag = Arc::clone(&error_flag);
        let source_config = source_config.clone();

        let handle = tokio::spawn(async move {
            worker_main(
                worker_id,
                source_config,
                work_rx,
                state,
                &r_id,
                &pg_id,
                lsn,
                batch_size,
                &db_name,
                event_tx,
                error_flag,
            )
            .await
        });

        handles.push(handle);
    }

    // ── 6. Wait for all workers to complete ──────────────────────
    let mut worker_errors: Vec<TapError> = Vec::new();

    for handle in handles {
        match handle.await {
            Ok(Ok(())) => {} // worker finished successfully
            Ok(Err(e)) => {
                error!("worker failed: {e}");
                worker_errors.push(e);
            }
            Err(join_err) => {
                error!("worker task panicked: {join_err}");
                worker_errors.push(TapError::Snapshot(format!(
                    "worker task panicked: {join_err}"
                )));
            }
        }
    }

    if !worker_errors.is_empty() {
        return Err(TapError::Snapshot(format!(
            "{} worker(s) failed: {:?}",
            worker_errors.len(),
            worker_errors
        )));
    }

    // ── 7. Compute final result ──────────────────────────────────
    let total_rows = {
        let store = state.lock().await;
        store.snapshot_chunk_total_rows(run_id)?
    };

    info!(
        run_id = %run_id,
        lsn = %lsn,
        total_rows,
        tables = %tables_snapshotted.len(),
        "parallel snapshot complete"
    );

    Ok(SnapshotResult {
        snapshot_id: run_id.to_string(),
        position: lsn,
        total_rows,
        tables_snapshotted,
    })
}

