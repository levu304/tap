//! Parallel Snapshot Engine — concurrent table-snapshot with PK-range chunking.
//!
//! # Architecture
//!
//! A **keeper** connection holds a `pg_export_snapshot()` for the entire
//! snapshot run.  N **worker** tasks (controlled by `num_workers`) compete
//! for chunks from a shared [`tokio::sync::mpsc`] work queue, each scanning
//! its assigned PK range via a server-side cursor.
//!
//! All workers pin their transactions to the same exported snapshot ID,
//! guaranteeing a globally consistent view.
//!
//! # Chunking
//!
//! Each table is divided into `num_workers × 2` chunks (over-subscription
//! for load balance).  Chunks are generated by querying `MIN(pk)/MAX(pk)`
//! and splitting the numeric range evenly.  Non-integer or composite PKs
//! fall back to a single chunk per table.
//!
//! # Resume
//!
//! On restart, the engine reads previously persisted chunk status from the
//! [`StateStore`].  `completed` chunks are skipped; `failed` and
//! `in_progress` chunks are re-issued.

use std::sync::Arc;
use std::time::{SystemTime, UNIX_EPOCH};

use tokio::sync::Mutex;
use tokio::sync::mpsc::{self, UnboundedSender};
use tracing::{error, info, warn};

