/// Main loop for a single worker task.
///
/// 1. Opens a plain connection to Postgres.
/// 2. Pins a REPEATABLE READ transaction to the exported snapshot.
/// 3. Loops: pops a chunk from the shared work queue → scans it → records
///    progress → marks chunk complete.
///
/// * `run_id` — Persistent chunk-grouping key (for state store operations).
/// * `pg_snapshot_id` — Postgres export-snapshot ID (for `SET TRANSACTION
///   SNAPSHOT`).
#[allow(clippy::too_many_arguments)]
async fn worker_main(
    worker_id: u32,
    source_config: SourceConfig,
    work_rx: Arc<Mutex<mpsc::Receiver<SnapshotChunk>>>,
    state: Arc<Mutex<StateStore>>,
    run_id: &str,
    pg_snapshot_id: &str,
    lsn: Lsn,
    batch_size: u32,
    db_name: &str,
    event_tx: UnboundedSender<ChangeEvent>,
    error_flag: Arc<std::sync::atomic::AtomicBool>,
) -> Result<(), TapError> {
    info!(worker_id, "worker starting");

    // ── Open worker connection ───────────────────────────────────
    let (mut client, handle) = connect_plain(&source_config).await?;

    // Run the worker loop
    let result = worker_loop(
        &mut client,
        worker_id,
        work_rx,
        state,
        run_id,
        pg_snapshot_id,
        lsn,
        batch_size,
        db_name,
        &event_tx,
        error_flag,
    )
    .await;

    // Drop client before awaiting the handle to avoid deadlock
    drop(client);
    handle.await.ok();

    result
}

/// Inner loop: claim chunks, scan, record progress.
///
/// * `run_id` — Persistent chunk-grouping key (for state store operations).
/// * `pg_snapshot_id` — Postgres export-snapshot ID for `SET TRANSACTION
///   SNAPSHOT`.
#[allow(clippy::too_many_arguments)]
async fn worker_loop(
    client: &mut tokio_postgres::Client,
    worker_id: u32,
    work_rx: Arc<Mutex<mpsc::Receiver<SnapshotChunk>>>,
    state: Arc<Mutex<StateStore>>,
    run_id: &str,
    pg_snapshot_id: &str,
    lsn: Lsn,
    batch_size: u32,
    db_name: &str,
    event_tx: &UnboundedSender<ChangeEvent>,
    error_flag: Arc<std::sync::atomic::AtomicBool>,
) -> Result<(), TapError> {
    // Start a REPEATABLE READ transaction pinned to the exported snapshot
    let txn = client.transaction().await?;
    txn.batch_execute("SET TRANSACTION ISOLATION LEVEL REPEATABLE READ")
        .await?;
    txn.simple_query(&format!("SET TRANSACTION SNAPSHOT '{pg_snapshot_id}'"))
        .await?;

    let ts_ms = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_millis() as u64)
        .unwrap_or(0);

    loop {
        // Check if another worker already failed
        if error_flag.load(std::sync::atomic::Ordering::Relaxed) {
            info!(worker_id, "shutting down due to sibling failure");
            break;
        }

        // Pop a chunk from the shared work queue
        let chunk: SnapshotChunk = {
            let mut rx = work_rx.lock().await;
            match rx.recv().await {
                Some(c) => {
                    // Mark in_progress immediately while holding the lock
                    // so no other worker claims it.
                    c
                }
                None => {
                    info!(worker_id, "no more chunks — done");
                    break;
                }
            }
        };

        // Mark in_progress in state store
        {
            let store = state.lock().await;
            store.start_chunk(run_id, &chunk.table_name, chunk.chunk_index)?;
        }

        // ── Detect PK for this table ─────────────────────────────
        let table = parse_qualified_name(&chunk.table_name);
        let pk_cols = detect_pk_columns(txn.client(), &table).await?;

        // ── Scan the chunk ───────────────────────────────────────
        let result = scan_chunk(
            txn.client(),
            &table,
            &pk_cols,
            &chunk,
            batch_size,
            ts_ms,
            &lsn,
            db_name,
            event_tx,
        )
        .await;

        match result {
            Ok(rows_count) => {
                // Mark chunk completed
                {
                    let store = state.lock().await;
                    store.complete_chunk(
                        run_id,
                        &chunk.table_name,
                        chunk.chunk_index,
                        rows_count,
                    )?;
                }
                info!(
                    worker_id,
                    table = %chunk.table_name,
                    chunk = chunk.chunk_index,
                    rows_count,
                    "chunk completed"
                );
            }
            Err(e) => {
                error!(
                    worker_id,
                    table = %chunk.table_name,
                    chunk = chunk.chunk_index,
                    error = %e,
                    "chunk failed"
                );
                // Record failure and set error flag
                {
                    let store = state.lock().await;
                    store.fail_chunk(
                        run_id,
                        &chunk.table_name,
                        chunk.chunk_index,
                        &e.to_string(),
                    )?;
                }
                error_flag.store(true, std::sync::atomic::Ordering::Relaxed);
                return Err(e);
            }
        }
    }

    txn.commit().await?;
    Ok(())
}

// ---------------------------------------------------------------------------
// Chunk scanner
// ---------------------------------------------------------------------------

/// Scan a single PK-range chunk using a server-side cursor.
///
/// Emits one `ChangeEvent` (op: Read) per row and returns the total row
/// count for the chunk.
#[allow(clippy::too_many_arguments)]
async fn scan_chunk(
    client: &tokio_postgres::Client,
    table: &TableInfo,
    pk_columns: &[String],
    chunk: &SnapshotChunk,
    batch_size: u32,
    ts_ms: u64,
    snapshot_lsn: &Lsn,
    db_name: &str,
    event_tx: &UnboundedSender<ChangeEvent>,
) -> Result<u64, TapError> {
    let sql_table = qualified_sql(table);
    let order_clause = if pk_columns.is_empty() {
        "ctid".to_string()
    } else {
        pk_columns
            .iter()
            .map(|c| quote_ident(c))
            .collect::<Vec<_>>()
            .join(", ")
    };

    // Build WHERE clause from chunk bounds
    let where_clause = if let Some(pk_col) = pk_columns.first() {
        let (clause, _, _) = chunk.where_clause(pk_col);
        if clause.is_empty() {
            String::new()
        } else {
            format!("WHERE {}", clause.trim_start_matches("WHERE "))
        }
    } else {
        String::new()
    };

    let query = format!("SELECT * FROM {sql_table} {where_clause} ORDER BY {order_clause}, ctid");

    // Declare cursor
    client
        .batch_execute(&format!("DECLARE chunk_cursor CURSOR FOR {query}"))
        .await?;

    let mut rows_done: u64 = 0;

    // Fetch loop
    loop {
        let fetch_sql = format!("FETCH FORWARD {} FROM chunk_cursor", batch_size);
        let batch = client.query(&fetch_sql, &[]).await?;

        if batch.is_empty() {
            break;
        }

        for row in &batch {
            emit_row_event(
                event_tx,
                table,
                pk_columns,
                snapshot_lsn,
                ts_ms,
                db_name,
                row,
            )?;
        }

        rows_done += batch.len() as u64;
    }

    // Cleanup cursor
    client.batch_execute("CLOSE chunk_cursor").await?;

    Ok(rows_done)
}

