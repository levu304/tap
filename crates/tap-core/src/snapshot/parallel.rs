    #[test]
    fn test_scan_query_structure() {
        let chunk = SnapshotChunk::new(
            "public.users".into(),
            "snap_1".into(),
            0,
            Some("1".into()),
            Some("1001".into()),
        );
        let table = parse_qualified_name("public.users");
        let pk_cols = vec!["id".to_string()];

        let pk_col = pk_cols.first().unwrap();
        let (clause, _, _) = chunk.where_clause(pk_col);

        let sql_table = qualified_sql(&table);
        let order_clause = pk_cols
            .iter()
            .map(|c| quote_ident(c))
            .collect::<Vec<_>>()
            .join(", ");

        let query =
            format!("SELECT * FROM {sql_table} WHERE {clause} ORDER BY {order_clause}, ctid");

        assert!(query.contains("SELECT * FROM \"public\".\"users\""));
        assert!(query.contains(r#""id" >= 1"#), "query={query}");
        assert!(query.contains(r#""id" < 1001"#), "query={query}");
        assert!(query.contains("ORDER BY \"id\", ctid"));
    }
